// ตัวอย่าง string
let username: string = "Alice";
let welcomeMessage: string = `Welcome, ${username}!`;
console.log(welcomeMessage); // Welcome, Alice!

// ตัวอย่าง number
let score: number = 95;
let bonus: number = 5;
let totalScore: number = score + bonus;
console.log("Total score:", totalScore); // Total score: 100

// ตัวอย่าง boolean
let isLoggedIn: boolean = true;
if (isLoggedIn) {
  console.log("Access granted."); // Access granted.
} else {
  console.log("Access denied.");
}
